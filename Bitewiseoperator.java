package bitewiseoperater;

public class Bitewiseoperator {
	
	public static void main(String a[])
	{
		int b, c, d, e;
		
		b = 25;
		c = 15;
		
		d = b & c; //--> AND operator
		
		e = b | c; //--> OR operator
		
		System.out.println("The AND operator result " + d);
		System.out.println("The OR operator result "+ e);
		
		
		
	}

}
